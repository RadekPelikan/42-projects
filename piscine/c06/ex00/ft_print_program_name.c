#include <unistd.h>
#include <stdio.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

int	main(int argc, char **argv)
{
	(void) argc;
	write(1, *argv, ft_strlen(*argv));
	write(1, "\n", 1);
	return (0);
}
